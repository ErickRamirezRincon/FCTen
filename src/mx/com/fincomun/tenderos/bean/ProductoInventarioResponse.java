package mx.com.fincomun.tenderos.bean;

import java.util.ArrayList;
import java.util.List;

public class ProductoInventarioResponse extends Response {

	private List<ProductoInventario> productos = new ArrayList<ProductoInventario>();
	
	public List<ProductoInventario> getProductos() {
		return productos;
	}

	public void setProductos(List<ProductoInventario> productos) {
		this.productos = productos;
	}

	@Override
	public String toString() {
		return "ProductoInventarioResponse [productos SIZE =" + productos.size() + ", getCodError()=" + getCodError()
				+ ", getMsjError()=" + getMsjError() + "]";
	}
	
}